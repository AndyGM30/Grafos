    private void btnMAActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMAActionPerformed
       boolean m[][] = grafo.matrizAd();
       String es = "   ";
       for(int a = 0; a < cmbV.getItemCount(); a++){
           es += cmbV.getItemAt(a) + " ";
       }
       for(int a = 0; a < cmbV.getItemCount(); a++){
           es += "\n" + cmbV.getItemAt(a) + " ";
           for (int b = 0; b < cmbV.getItemCount(); b++) {
                es += m[a][b] ? "1 " : "0 ";
            }
       }
       txtMostrarVA.setText(es);
    }//GEN-LAST:event_btnMAActionPerformed

