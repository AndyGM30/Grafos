    private void btnAVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAVActionPerformed
        try{
        char v =  txtVertice.getText().toUpperCase().charAt(0);
        if(grafo.insertarVertice(v)){
            cmbDe.addItem(v);
            cmbV.addItem(v);
            cmbHasta.addItem(v);
        }txtVertice.setText("");
        }catch(StringIndexOutOfBoundsException e){
            showMessageDialog(null,"Inserta un dato");
            txtVertice.requestFocus();
        }
    }//GEN-LAST:event_btnAVActionPerformed

    private void btnEVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEVActionPerformed
        try{
        char v =  (char)cmbV.getSelectedItem();
        if(grafo.eliminarVertice(v)){
            cmbDe.removeItem(v);
            cmbV.removeItem(v);
            cmbHasta.removeItem(v);
            return;
        }
        }catch(NullPointerException n){
            showMessageDialog(null,"Error, NO hay vértices a eliminar");
            txtVertice.requestFocus();
        }
    }//GEN-LAST:event_btnEVActionPerformed

